package Euler;

import java.math.BigDecimal;

/**
 * Simple class to hold coordinates of a point for the purpose of data encapsulation.
 */
public class Point {
    private double x;
    private double y;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
